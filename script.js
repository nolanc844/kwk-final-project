function changeToBeach() {
    var beachChange = document.getElementById("background");
    beachChange.setAttribute("src", "#Hanola");
    var byeSphere1 = document.getElementById("sphere1");
    byeSphere1.setAttribute("visible", "false");
    var byeSphere2 = document.getElementById("sphere2");
    byeSphere2.setAttribute("visible", "false");
    var byeSphere3 = document.getElementById("sphere3");
    byeSphere3.setAttribute("visible", "false")
}

